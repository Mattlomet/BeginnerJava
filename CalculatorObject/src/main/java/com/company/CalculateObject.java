package com.company;

public class CalculateObject {

    public void addTwo(int x,int y){
        System.out.println(x+y);
    };
    public void subtractTwo(int x,int y){
        System.out.println(x-y);
    };
    public void multiplyTwo(int x,int y){
        System.out.println(x*y);
    };
    public void divideTwo(int x,int y){
        System.out.println(x/y);
    };

    public void addTwo(double x,double y){
        System.out.println(x+y);
    };
    public void subtractTwo(double x,double y){
        System.out.println(x-y);
    };
    public void multiplyTwo(double x,double y){
        System.out.println(x*y);
    };
    public void divideTwo(double x,double y){
        System.out.println(x/y);
    };
}
